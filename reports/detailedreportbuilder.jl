println(io,"using Plots,DataFrames,CSV")
println(io,"pdf = CSV.read(\"../data/params.csv\",DataFrame)")
println(io,"pdf=filter(row->row.cell==$REPORT,pdf)")
println(io,"sort!(pdf,:cycle)")
