for r in 1:nrow(df)
    println(io,"#' #### VAH: $REPORT; CYCLE: $(df.cycle[r])")
    println(io,"#+ echo=false")
    println(io,"cell=$(REPORT)")
    println(io,"cycle=$(df.cycle[r])")
    println(io,"params = pdf[$r,:]")
    println(io,"include(\"evaluatorsetup.jl\")")
