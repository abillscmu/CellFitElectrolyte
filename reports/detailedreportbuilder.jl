mydt = Dates.format(dt, "yyyy-mm-dd HH:MM:SS")

report_name = "reports/detailedreportVAH$REPORT.jl"
io = open(report_name,"w")

println(io,"#' # Detailed Report for VAH $REPORT")
println(io,"#' #### Generated by Alec Bills at $mydt")
